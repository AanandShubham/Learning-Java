// Write a Program to swap two Numbers
public class Swap {
    public static void main(String... args){
        int a = 23;
        int b = 24;
        System.out.println("Before swap : a =  " + a + " b = " + b);
        int t = a;
        a=b;
        b=t;
        System.out.println("After swap : a =  " + a + " b = " + b);

    }
}
